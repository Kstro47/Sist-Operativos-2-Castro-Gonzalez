    public MainUI() {
        initComponents();
        this.setVisible(true);
        this.setLocationRelativeTo(null);
        this.setResizable(false);
        
        Studio StudioNK = new Studio("NK");
        Studio StudioCN = new Studio("CN");
        AI ai = new AI();
        Administrator admin = new Administrator(StudioNK, StudioCN);
        this.simulation = new BattleSimulation(ai, admin);
        
        
        admin.chooseCharacter(ai);
        this.simulation.start();
        
        IAtim.setText(Integer.toString(this.simulation.ai.time));
        while (true){
            if (this.simulation.ai.CharA != null) {   
                try {
                    IAState.setText(this.simulation.ai.state);
                    ImgCharA.setIcon(new ImageIcon(ImageIO.read(new File(this.simulation.ai.CharA.charImage))));
                    ImgCharB.setIcon(new ImageIcon(ImageIO.read(new File(this.simulation.ai.CharB.charImage))));
                    CharAName.setText(this.simulation.ai.CharA.name);
                    CharBName.setText(this.simulation.ai.CharB.name);
                    CharAHealth.setText(Integer.toString(this.simulation.ai.CharA.health));
                    CharBHealth.setText(Integer.toString(this.simulation.ai.CharB.health));
                    CharASkill.setText(Integer.toString(this.simulation.ai.CharA.skill));
                    CharBSkill.setText(Integer.toString(this.simulation.ai.CharB.skill));
                    CharAAgility.setText(Integer.toString(this.simulation.ai.CharA.agility));
                    CharBAgility.setText(Integer.toString(this.simulation.ai.CharB.agility));
                    CharAStrength.setText(Integer.toString(this.simulation.ai.CharA.strength));
                    CharBStrength.setText(Integer.toString(this.simulation.ai.CharB.strength));
                    
                    showCola1.setText(this.simulation.StudioNK.cola1.showCola());
                    showCola2.setText(this.simulation.StudioNK.cola2.showCola());
                    showCola3.setText(this.simulation.StudioNK.cola3.showCola());
                    showCola4.setText(this.simulation.StudioCN.cola1.showCola());
                    showCola5.setText(this.simulation.StudioCN.cola2.showCola());
                    showCola6.setText(this.simulation.StudioCN.cola3.showCola());
                    
                    winsNK.setText(Integer.toString(this.simulation.StudioNK.winners.getSize()));
                    winsCN.setText(Integer.toString(this.simulation.StudioCN.winners.getSize()));
                    
                    
                    if ("Decidiendo resultado".equals(this.simulation.ai.state)) {
                        if (this.simulation.ai.stateInt == 1) {
                            jTextArea2.setText(jTextArea2.getText() + "\n\n" + this.simulation.ai.startText + "\n");
                            this.simulation.ai.stateInt = 0;
                        }  
                    } else {
                        if (this.simulation.ai.stateInt == 1) {
                            jTextArea2.setText(this.simulation.ai.startText + "\n" + this.simulation.ai.announceText + this.simulation.ai.finalText );
                            this.simulation.ai.announceText = "";
                            this.simulation.ai.stateInt = 0;
                        }
                    }
                    
                    
                  
                    
                } catch (IOException ex) {
                    Logger.getLogger(MainUI.class.getName()).log(Level.SEVERE, null, ex);
                }  
            }    
        }
        
    }

    
    
    
    
    
    
